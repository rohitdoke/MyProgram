//Write a java program to display from 20 to 1 using while loop.

package com.justimagine.Loop;

public class ReverseNubmber {

	public static void main(String[] args) {
		int i=20;
		
		System.out.println("Number between 20 to 1 are:");
		while(i>=1)
		{
			System.out.println(i);			
			i--;
		}
		

	}

}
