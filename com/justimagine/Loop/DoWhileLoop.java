//Write a java program to display from 1 to 20 using do while loop.
package com.justimagine.Loop;

public class DoWhileLoop {

	public static void main(String[] args) {
		System.out.println("Number between 1 to 20 using do while loop:");

		
		int i=1;
		do {
			
			
			System.out.println(i);
			i++;
			
		}while(i<=20);
	}

}
