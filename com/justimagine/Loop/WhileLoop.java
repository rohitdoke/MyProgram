	//Write a java program to display from 1 to 20 using while loop.

package com.justimagine.Loop;

public class WhileLoop {

	public static void main(String[] args) {
		int i=1;
		
		System.out.println("Number between 1 to 20 are:");
		while(i<=20)
		{
			System.out.println(i);			
			i++;
		}
		

	}

}
